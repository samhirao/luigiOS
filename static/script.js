function typeMessage(message, outputDiv, className) {
    const textElement = document.createElement("div");
    textElement.classList.add(className);
    outputDiv.appendChild(textElement);

    let index = 0;
    const typingSpeed = 10;
    const typingInterval = setInterval(() => {
        textElement.textContent += message[index];
        index++;

        if (index === message.length) {
            clearInterval(typingInterval);
        }
    }, typingSpeed);
}

userInput.addEventListener("keydown", function(event) {
    if (event.key === "Enter") {
        const prompt = userInput.value;
        
        if (!prompt) return;

        const userText = document.createElement("div");
        userText.classList.add("user-text");
        userText.textContent = "> User: " + prompt;
        outputDiv.appendChild(userText);

        const thinkingMessage = "Hmm... Let's-a-think!";
        typeMessage(thinkingMessage, outputDiv, "thinking-text");

        outputDiv.scrollTop = outputDiv.scrollHeight;

        fetch("/api/query", {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify({ prompt: prompt })
        })
        .then(response => response.json())
        .then(data => {
            const thinkingTextElements = document.querySelectorAll(".thinking-text");
            thinkingTextElements.forEach(element => element.remove());

            if (data.response) {
                typeMessage("> Luigi: " + data.response, outputDiv, "ai-text");
            } else if (data.error) {
                const errorText = document.createElement("div");
                errorText.classList.add("ai-text");
                errorText.textContent = "Error: " + data.error;
                outputDiv.appendChild(errorText);
            }

            outputDiv.scrollTop = outputDiv.scrollHeight;

            userInput.value = "";
            userInput.focus();
        })
        .catch(error => {
            console.error("Error:", error);
            const errorText = document.createElement("div");
            errorText.classList.add("ai-text");
            errorText.textContent = "Error: " + error.message;
            outputDiv.appendChild(errorText);
        });
    }
});
